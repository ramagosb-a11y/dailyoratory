function mergeByDate<T extends { date: string }>(baseRecords: T[], overrideRecords: T[]) {
  const recordsByDate = new Map<string, T>();

  for (const record of baseRecords) recordsByDate.set(record.date, record);
  for (const record of overrideRecords) recordsByDate.set(record.date, record);

  return Array.from(recordsByDate.values()).sort((a, b) => a.date.localeCompare(b.date));
}

