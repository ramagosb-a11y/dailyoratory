export function getOAntiphons() {
  return [...oAntiphons].sort((a, b) => a.sortOrder - b.sortOrder);
}

export function getTodaysOAntiphon(date = new Date()) {
  return oAntiphons.find((antiphon) => antiphon.dateMonth === date.getMonth() + 1 && antiphon.dateDay === date.getDate()) ?? null;
}
