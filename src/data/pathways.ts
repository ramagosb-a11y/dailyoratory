const staticSource = { system: "static" as const };
const createdAt = "2026-05-06";
const updatedAt = "2026-05-06";

type PathwayInput = Omit<
  SpiritualGrowthPathwayRecord,
  | "status"
  | "createdAt"
  | "updatedAt"
  | "visibility"
  | "source"
  | "schemaVersion"
  | "contentType"
  | "category"
  | "stepTimeline"
>;

function createPathway(input: PathwayInput): SpiritualGrowthPathwayRecord {
  return {
    ...input,
    category: "Formation",
    status: "published",
    createdAt,
    updatedAt,
    visibility: "public",
    source: staticSource,
    schemaVersion: 1,
    contentType: "spiritual-growth-pathway",
    stepTimeline: input.modules.map((module) => `${module.timeframe}: ${module.title}`),
  };
}

function step(
  pathwaySlug: string,
  order: number,
  slug: string,
  title: string,
  description: string,
  teaching: string,
  scriptureReference: string,
  prayerPrompt: string,
  practice: string,
  reflectionQuestions: string[],
  relatedResourceIds: string[] = [],
): PathwayStep {
  return {
    id: `${pathwaySlug}-step-${order}`,
    title,
    slug,
    description,
    order,
    timeframe: `Week ${order}`,
    teaching,
    scriptureReference,
    prayerPrompt,
    practice,
    reflectionQuestions,
    relatedResourceIds,
  };
}

function audienceTags(values: string[]) {
  return values;
}

