type ApprovedIntentionInput = {
  id: string;
  title: string;
  slug: string;
  description: string;
  tags: string[];
  relatedResourceIds: string[];
  createdAt: string;
  updatedAt: string;
  intentionType: PrayerIntentionRecord["intentionType"];
  requesterDisplayName: string;
  isAnonymous: boolean;
  submittedAt: string;
  approvedAt: string;
  intentionStatus?: PrayerIntentionRecord["intentionStatus"];
  prayedCount: number;
  isUrgent?: boolean;
  isThanksgiving?: boolean;
  excerpt: string;
  prayerPrompt: string;
};

function approved(input: ApprovedIntentionInput): PrayerIntentionRecord {
  return {
    id: input.id,
    title: input.title,
    slug: input.slug,
    description: input.description,
    category: "Prayer Intention",
    tags: input.tags,
    relatedResourceIds: input.relatedResourceIds,
    status: "published",
    createdAt: input.createdAt,
    updatedAt: input.updatedAt,
    visibility: "public",
    source: staticSource,
    schemaVersion: 1,
    contentType: "prayer-intention",
    intentionType: input.intentionType,
    intentionVisibility: input.isAnonymous ? "anonymous-public" : "public",
    requesterDisplayName: input.requesterDisplayName,
    isAnonymous: input.isAnonymous,
    submittedAt: input.submittedAt,
    approvedAt: input.approvedAt,
    intentionStatus: input.intentionStatus ?? "open",
    moderationStatus: "approved",
    prayedCount: input.prayedCount,
    reportCount: 0,
    isUrgent: Boolean(input.isUrgent),
    isThanksgiving: Boolean(input.isThanksgiving || input.intentionType === "thanksgiving"),
    excerpt: input.excerpt,
    prayerPrompt: input.prayerPrompt,
    moderationFlags: [],
  };
}
