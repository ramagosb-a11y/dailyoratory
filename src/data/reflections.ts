import { massReadingsReflections } from "@/data/massReadingsReflections";
import type { DailyReflectionRecord } from "@/types/reflections";

export const dailyReflectionRecords: DailyReflectionRecord[] = massReadingsReflections.map((reflection) => ({
  id: reflection.id,
  title: reflection.title,
  slug: reflection.slug,
  description: reflection.description,
  category: reflection.category,
  tags: reflection.tags,
  relatedResourceIds: reflection.relatedResourceIds,
  status: reflection.status,
  createdAt: reflection.createdAt,
  updatedAt: reflection.updatedAt,
  visibility: reflection.visibility,
  source: reflection.source,
  provenance: reflection.provenance,
  sourceNotes: reflection.sourceNotes,
  copyrightStatus: reflection.copyrightStatus,
  attributionText: reflection.attributionText,
  contentAuthor: reflection.contentAuthor,
  contentReviewer: reflection.contentReviewer,
  schemaVersion: reflection.schemaVersion,
  contentType: "daily-reflection",
  reflectionKind: reflection.reflectionType === "daily-mass" ? "daily" : "sunday",
  date: reflection.reflectionDate,
  liturgicalDay: `${reflection.liturgicalDay} - Lectionary: ${reflection.lectionaryNumber}`,
  season: reflection.liturgicalSeason,
  scriptureReferences: reflection.readings.map((reading) => ({
    label: reading.label,
    citation: reading.reference,
  })),
  excerpt: reflection.shortDescription,
  body: reflection.body,
  reflectionQuestions: reflection.practicalApplication,
  prayer: reflection.prayers[0]?.body ?? "",
  topics: [reflection.theme, ...reflection.theologicalInsights].filter(Boolean),
  externalReadingsUrl: reflection.externalReadingsUrl,
  canonicalPath: reflection.canonicalPath,
}));
