export const rosaryMysteries: RosaryMystery[] = rosaryMysteryDrafts.map((mystery) => {
  const groupMysteries = rosaryMysteryDrafts
    .filter((item) => item.groupSlug === mystery.groupSlug)
    .sort((first, second) => first.sortOrder - second.sortOrder);
  const index = groupMysteries.findIndex((item) => item.slug === mystery.slug);

  return {
    ...mystery,
    previousMysterySlug: groupMysteries[index - 1]?.slug ?? null,
    nextMysterySlug: groupMysteries[index + 1]?.slug ?? null,
  };
});

