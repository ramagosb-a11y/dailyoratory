function toOrdinalWord(decadeNumber: number) {
  switch (decadeNumber) {
    case 1:
      return "First";
    case 2:
      return "Second";
    case 3:
      return "Third";
    case 4:
      return "Fourth";
    case 5:
      return "Fifth";
    default:
      return `${decadeNumber}th`;
  }
}

function createDecadeSteps(
  mysteryLabel: string,
  title: string,
  scriptureReference: string,
  fruitOfMystery: string,
  graceToAsk: string,
): RosaryDecadeStep[] {
  return [
    {
      id: `${title}-announce`.toLowerCase().replaceAll(" ", "-"),
      title: "Announce the mystery",
      instruction: `Say: ${mysteryLabel}, ${title}.`,
      sortOrder: 10,
    },
    {
      id: `${title}-scripture`.toLowerCase().replaceAll(" ", "-"),
      title: "Read or recall the Scripture scene",
      instruction: `Recall the Gospel scene in ${scriptureReference} and place yourself prayerfully near Jesus and Mary.`,
      sortOrder: 20,
    },
    {
      id: `${title}-our-father`.toLowerCase().replaceAll(" ", "-"),
      title: "Pray one Our Father",
      instruction: "Offer the decade to the Father through Jesus in the Holy Spirit.",
      prayerText: "Our Father",
      sortOrder: 30,
    },
    {
      id: `${title}-hail-marys`.toLowerCase().replaceAll(" ", "-"),
      title: "Pray ten Hail Marys slowly",
      instruction: "Let each Hail Mary keep you close to the mystery instead of rushing through the words.",
      prayerText: "Hail Mary",
      sortOrder: 40,
    },
    {
      id: `${title}-glory-be`.toLowerCase().replaceAll(" ", "-"),
      title: "Pray one Glory Be",
      instruction: "Return praise to the Father, Son, and Holy Spirit for the grace hidden in this mystery.",
      prayerText: "Glory Be",
      sortOrder: 50,
    },
    {
      id: `${title}-fatima-prayer`.toLowerCase().replaceAll(" ", "-"),
      title: "Pray the Fatima Prayer, if desired",
      instruction: "Add the customary prayer after the Glory Be if it helps you remain recollected.",
      prayerText: "O my Jesus...",
      sortOrder: 60,
    },
    {
      id: `${title}-fruit`.toLowerCase().replaceAll(" ", "-"),
      title: "Ask for the fruit of the mystery",
      instruction: `Ask for ${fruitOfMystery.toLowerCase()} and for the grace ${graceToAsk.toLowerCase()}.`,
      sortOrder: 70,
    },
    {
      id: `${title}-silence`.toLowerCase().replaceAll(" ", "-"),
      title: "Rest briefly in silence",
      instruction: "Stay for a moment with Jesus and Mary before moving on to the next mystery.",
      sortOrder: 80,
    },
  ];
}

function createMystery({
  groupSlug,
  title,
  slug,
  decadeNumber,
  scriptureReference,
  scriptureSummary,
  openingPrayer,
  sensoryMeditation,
  scriptureForMeditation,
  contemplativePause,
  meditationConclusionPrompt,
  optionalClosingPrayer,
  expandedFruitReflection,
  extendedMeditation,
  contemplationPoints,
  virtue,
  fruitOfMystery,
  graceToAsk,
  decadeIntention,
  decadePrayer,
  howToPrayThisDecade,
  closingPrayer,
  practiceToday,
  meditationPrompt,
  reflectionQuestions,
  relatedLinks,
}: {
  groupSlug: RosaryMysteryGroupSlug;
  title: string;
  slug: string;
  decadeNumber: number;
  scriptureReference: string;
  scriptureSummary: string;
  openingPrayer?: string;
  sensoryMeditation?: RosarySensoryMeditation[];
  scriptureForMeditation?: RosaryScriptureForMeditation;
  contemplativePause?: string;
  meditationConclusionPrompt?: string;
  optionalClosingPrayer?: string;
  expandedFruitReflection?: string;
  extendedMeditation: string[];
  contemplationPoints: string[];
  virtue: string;
  fruitOfMystery: string;
  graceToAsk: string;
  decadeIntention: string;
  decadePrayer: string;
  howToPrayThisDecade?: RosaryDecadeStep[];
  closingPrayer: string;
  practiceToday: string;
  meditationPrompt: string;
  reflectionQuestions: string[];
  relatedLinks?: RosaryLink[];
}): RosaryMystery {
  const group = groupBySlug[groupSlug];
  const decadeWord = toOrdinalWord(decadeNumber);
  const decadeLabel = `${decadeWord} ${group.shortTitle} Decade`;
  const mysteryLabel = `${decadeWord} ${group.shortTitle} Mystery`;
  const fullPath = `/devotions/holy-rosary/${groupSlug}/${slug}`;

  return {
    id: `${groupSlug}-${slug}`,
    groupId: group.id,
    groupSlug,
    title,
    slug,
    fullPath,
    fullSlug: fullPath,
    decadeNumber,
    decadeLabel,
    mysteryLabel,
    scriptureReference,
    scriptureSummary,
    openingPrayer,
    sensoryMeditation,
    scriptureForMeditation,
    contemplativePause,
    meditationConclusionPrompt,
    optionalClosingPrayer,
    expandedFruitReflection,
    extendedMeditation,
    contemplationPoints,
    virtue,
    fruitOfMystery,
    graceToAsk,
    decadeIntention,
    decadePrayer,
    reflectionQuestions,
    howToPrayThisDecade:
      howToPrayThisDecade ??
      createDecadeSteps(mysteryLabel, title, scriptureReference, fruitOfMystery, graceToAsk),
    closingPrayer,
    relatedLinks: relatedLinks ?? commonMysteryRelatedLinks,
    previousMysterySlug: null,
    nextMysterySlug: null,
    practiceToday,
    meditationPrompt,
    meditation: extendedMeditation.join("\n\n"),
    prayer: decadePrayer,
    sortOrder: decadeNumber,
  };
}

