function practice(id: string, title: string, description: string, cadence: "daily" | "weekly" | "when-tempted") {
  return { id, title, description, cadence };
}

function virtueRecord({
  virtue,
  title,
  family,
  description,
  tags,
  shortPrayer,
  reflectionPrompt,
}: {
  virtue: VirtueName;
  title: string;
  family: VirtueDefinitionRecord["family"];
  description: string;
  tags: string[];
  shortPrayer: string;
  reflectionPrompt: string;
}): VirtueDefinitionRecord {
  return {
    id: `virtue-${virtue}`,
    title,
    slug: title.toLowerCase().replaceAll(" ", "-"),
    description,
    category: "Virtue",
    tags: ["virtue tracker", ...tags],
    relatedResourceIds: ["tracker-template-weekly-virtue-review", "exam-daily-review"],
    status: "published",
    createdAt: dateStamp,
    updatedAt: dateStamp,
    visibility: "public",
    source: staticSource,
    schemaVersion: 1,
    contentType: "virtue-definition",
    virtue,
    family,
    shortPrayer,
    reflectionPrompt,
    practices: [
      practice(`practice-${virtue}-notice`, "Notice one opportunity", "Name one concrete place this virtue can be practiced today.", "daily"),
      practice(`practice-${virtue}-prayer`, "Ask for grace", "Ask the Lord for grace before the difficult moment arrives.", "daily"),
    ],
  };
}

function viceRecord({
  vice,
  title,
  description,
  contraryVirtues,
  warningSigns,
  graceFilledNextSteps,
}: {
  vice: ViceName;
  title: string;
  description: string;
  contraryVirtues: VirtueName[];
  warningSigns: string[];
  graceFilledNextSteps: string[];
}): ViceDefinitionRecord {
  return {
    id: `vice-${vice}`,
    title,
    slug: title.toLowerCase().replaceAll(" ", "-"),
    description,
    category: "Virtue",
    tags: ["vice", "virtue tracker", vice, ...contraryVirtues],
    relatedResourceIds: ["exam-before-confession", "tracker-template-weekly-virtue-review"],
    status: "published",
    createdAt: dateStamp,
    updatedAt: dateStamp,
    visibility: "public",
    source: staticSource,
    schemaVersion: 1,
    contentType: "vice-definition",
    vice,
    contraryVirtues,
    gentleDescription: description,
    warningSigns,
    graceFilledNextSteps,
    confessionPrepPrompts: [
      "Was this a pattern I freely chose, or a temptation I resisted imperfectly?",
      "Do I need to name this simply in confession or ask the priest for guidance?",
      "Is there someone I should apologize to, repay, forgive, or protect?",
    ],
  };
}

